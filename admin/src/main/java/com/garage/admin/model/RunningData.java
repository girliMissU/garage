package com.garage.admin.model;

public class RunningData {
    private int id;
    private int status;
    private float leftW;
    private float leftN;
    private float leftI;
    private float rightW;
    private float rightN;
    private float rightI;
    private float horizontalW;
    private float horizontalN;
    private float horizontalI;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getStatus() {
        return status;
    }

    public void setStatus(int status) {
        this.status = status;
    }

    public float getLeftW() {
        return leftW;
    }

    public void setLeftW(float leftW) {
        this.leftW = leftW;
    }

    public float getLeftN() {
        return leftN;
    }

    public void setLeftN(float leftN) {
        this.leftN = leftN;
    }

    public float getLeftI() {
        return leftI;
    }

    public void setLeftI(float leftI) {
        this.leftI = leftI;
    }

    public float getRightW() {
        return rightW;
    }

    public void setRightW(float rightW) {
        this.rightW = rightW;
    }

    public float getRightN() {
        return rightN;
    }

    public void setRightN(float rightN) {
        this.rightN = rightN;
    }

    public float getRightI() {
        return rightI;
    }

    public void setRightI(float rightI) {
        this.rightI = rightI;
    }

    public float getHorizontalW() {
        return horizontalW;
    }

    public void setHorizontalW(float horizontalW) {
        this.horizontalW = horizontalW;
    }

    public float getHorizontalN() {
        return horizontalN;
    }

    public void setHorizontalN(float horizontalN) {
        this.horizontalN = horizontalN;
    }

    public float getHorizontalI() {
        return horizontalI;
    }

    public void setHorizontalI(float horizontalI) {
        this.horizontalI = horizontalI;
    }
}
